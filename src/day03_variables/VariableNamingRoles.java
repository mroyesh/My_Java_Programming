package day03_variables;
public class VariableNamingRoles {
    public static void main(String[] args) {
        // age of the student is 28
        //salary is 10000
        // we should use meaningful names for

        int age = 28;
        int salaryOfMyBoss = 10000;
        // once we declare variable  we can not use that/must be unique
        int phoneNumber = 619-317-8284;
        double salaryBeforeTax = 89000;
        double salaryAfterTax = 129000;

        /* 1. variable naming rules:
        1. readable, understandable, and meaningful.
        2. should be UNIQUE.
        3. Camel Case
         */
        int number1 = 10;
        int number2 = 20;
        int number3 = 30;
        double finalScore = 89.7;
        // gender is male
        String gender = "male";

        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(finalScore);
        System.out.println(gender);
    }
}
