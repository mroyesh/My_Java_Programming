package day29_inheritance.encapsulation;

public class Tester {
    private String name;
    private char gender;
    private int age;
    private String id;
    private String jobTitle;
    private double salary;

    public Tester(String name, char gender, int age, String id, String jobTitle, double salary) {
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.id = id;
        this.jobTitle = jobTitle;
        this.salary = salary;
    }
}
