package day02_escapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Students\nToday we have Java Class");
        System.out.println("\tJava Developer");
        System.out.println("\\");
        System.out.println("\"Being Misfit\"");
    }

    /*
    \n this scape sequence used for new line
    \t this scape used to make paragraph space
    \\ used to singe backslash
    \" Used to for double quote
     */

}
